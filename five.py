# Print only odd values in an array

x = input(list())
print (x)

for i in x:
    y = int(i)
    print (y)
    if y % 2 != 0:
        print (i)

# find common values between two arrays
x = input(list())
y = input(list())

print (x)
print (y)

for i in x:
    if i in y:
        print (i)
